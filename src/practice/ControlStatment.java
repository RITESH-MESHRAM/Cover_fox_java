package practice;

public class ControlStatment {

	public static void main(String[] args) {
		// CONTROL STATMENT
		char gender='m';
		switch(gender) {
		case 'M','m': System.out.println("He is Male ");	
		break;
		case 'F','f': System.out.println("She is Female");
		break;
		default : System.out.println("TransGender");
		break;
		
		}
		
		
	}

}
