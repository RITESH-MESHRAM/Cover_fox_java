package practice;

public class Switch {

	public static void main(String[] args) {
		
		//Switch Statment
		String month="No";
		
		switch(month) {
		case "Jan": System.out.println("1st month");
		break;
		
		case "Feb": System.out.println("2nd month");
		break;
		
		case "Mar": System.out.println("3rd month");
		break;
		
		case "Apr": System.out.println("4th month");
		break;
		
		case "may": System.out.println("5th month");
		break;
		
		case "Jun": System.out.println("6th month");
		break;
		
		case "Juj":System.out.println("7th month");
		break;
		
		case "Aug":System.out.println("8th month");
		break;
		
		case "Sep":System.out.println("9th month");
		break;
		
		case "Oct": System.out.println("10th month");
		break;
		
		case "Nov":System.out.println("11th month");
		break;
		
		case"Dec": System.out.println("12th month");
		break;
		
		default:
			System.out.println("Please enter valid month name ");
		
		}

	}

}
