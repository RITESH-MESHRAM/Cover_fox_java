package loops;

public class For_loop2 {

	public static void main(String[] args) {
		//TABLE OF 3
		for(int i=3;i<=30;i=i+3)
		{
			System.out.println(i);
		}
			System.out.println("=============================================");
			//TABLE OF 5
			for(int i=5;i<=50;i=i+5) //This is incremental for loop
			{
				System.out.println(i);
			}
			System.out.println("=============================================");
		
			for(int i=30;i>=3;i=i-3) //This is decremental for loop
			{
				System.out.println(i);
			}
			System.out.println("=============================================");
			for(int i=1;i<=5;i++)
			{
				System.out.println("Pune");
			}
			
			//Initilization
			int i = 1;
			while(i<=5) // Condition
		{
			System.out.println(2);
			i++;  //Updation
			} 
		}
	
	

}
